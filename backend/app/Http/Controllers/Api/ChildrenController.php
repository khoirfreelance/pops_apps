                'prov' => $data[8] ?? null,
                'kab' => $data[9] ?? null,
                'kec' => $data[10] ?? null,
                'puskesmas' => $data[11] ?? null,
                'desa' => $data[12] ?? null,
                'posyandu' => $data[13] ?? null,
                'rt' => $data[14] ?? null,
                'rw' => $data[15] ?? null,
                'alamat' => $data[16] ?? null,
                'usia_saat_ukur' => $data[17] ?? null,
                'tanggal_pengukuran' => $this->convertDate($data[18] ?? null),
